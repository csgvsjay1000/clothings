package ivg.cn.clo.factory.model.entity;

import java.util.Date;

public class FDeliveryBillTrace {
    private Long fid;

    private String billNum;

    private Integer billStatus;

    private String specificAction;

    private String operator;

    private Date createDate;

    private String creater;

    public Long getFid() {
        return fid;
    }

    public void setFid(Long fid) {
        this.fid = fid;
    }

    public String getBillNum() {
        return billNum;
    }

    public void setBillNum(String billNum) {
        this.billNum = billNum;
    }

    public Integer getBillStatus() {
        return billStatus;
    }

    public void setBillStatus(Integer billStatus) {
        this.billStatus = billStatus;
    }

    public String getSpecificAction() {
        return specificAction;
    }

    public void setSpecificAction(String specificAction) {
        this.specificAction = specificAction;
    }

    public String getOperator() {
        return operator;
    }

    public void setOperator(String operator) {
        this.operator = operator;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    public String getCreater() {
        return creater;
    }

    public void setCreater(String creater) {
        this.creater = creater;
    }
}